/**
 * Main Resend webhook endpoint - handles all email events
 */
router.post("/api/webhooks/resend", async (req, res) => {
  try {
    // Signature verification — fail-closed: reject if no secret is configured.
    // This prevents unauthenticated actors from spoofing bounce/complaint events
    // to suppress email addresses or manipulate delivery state.
    const outboundSignature = req.headers["svix-signature"] as string | undefined;
    const outboundTimestamp = req.headers["svix-timestamp"] as string | undefined;
    const outboundMsgId = req.headers["svix-id"] as string | undefined;
    const outboundRawBody = (req as any).rawBody || JSON.stringify(req.body);

    if (!verifyResendSignature(outboundRawBody, outboundSignature, outboundTimestamp, outboundMsgId)) {
      log.error("[Resend Webhook] Signature verification failed — rejecting outbound event webhook");
      return res.status(401).json({ error: "Invalid signature" });
    }

    const event: ResendWebhookEvent = req.body;

    if (!event || typeof event.type !== 'string') {
      log.error("[Resend Webhook] Malformed payload — missing event.type (type=%s)", typeof event?.type);
      return res.status(400).json({ error: "Invalid webhook payload" });
    }

    // Idempotency guard: deduplicate Resend webhook deliveries using svix-id header
    const svixId = req.headers['svix-id'] as string | undefined;
    if (svixId) {
      try {
        // CATEGORY C — Raw SQL retained: LIMIT | Tables: resend_webhook_events | Verified: 2026-03-23
        const existing = await typedQuery(
          sql`SELECT id FROM resend_webhook_events WHERE message_id = ${svixId} LIMIT 1`
        );
        if (existing.length > 0) {
          return res.status(200).json({ received: true, processed: false, reason: 'duplicate_delivery' });
        }
        // Converted to Drizzle ORM: ON CONFLICT
        await db.insert(resendWebhookEvents).values({
          messageId: svixId,
          eventType: event.type,
        }).onConflictDoNothing();
      } catch (idempotencyErr: unknown) {
        log.warn('[Resend Webhook] Idempotency check failed (non-blocking):', (idempotencyErr instanceof Error ? idempotencyErr.message : String(idempotencyErr)));
      }
    }
    
    switch (event.type) {
      case "email.delivered": {
        // Extract invoiceId from Resend tags set at send time
        const tags: Array<{ name: string; value: string }> = (event as any).data.tags || [];
        const invoiceTag = tags.find((t) => t.name === 'invoiceId');
        if (invoiceTag?.value) {
          const invoiceId = invoiceTag.value;
          try {
            // Fetch the invoice so we can include workspaceId in the audit entry
            const [inv] = await db
              .select({ id: invoices.id, workspaceId: invoices.workspaceId, invoiceNumber: invoices.invoiceNumber })
              .from(invoices)
              .where(eq(invoices.id, invoiceId))
              .limit(1);

            if (inv) {
              // Flip delivery_confirmed = true
              await db
                .update(invoices)
                .set({ deliveryConfirmed: true })
                .where(eq(invoices.id, invoiceId));

              // Write immutable audit trail entry
              await universalAudit.log({
                workspaceId: inv.workspaceId,
                action: 'invoice.delivery_confirmed' as any,
                actorType: 'system',
                actorId: 'resend-webhook',
                changeType: 'action',
                entityType: 'invoice',
                entityId: invoiceId,
                entityName: inv.invoiceNumber,
                metadata: {
                  invoiceNumber: inv.invoiceNumber,
                  deliveredTo: event.data.to?.join(', '),
                  resendEventAt: (event as any).data.created_at || new Date().toISOString(),
                  source: 'resend_email.delivered_webhook',
                },
              });

              log.info(`[Resend Webhook] delivery_confirmed=true for invoice ${inv.invoiceNumber} (${invoiceId})`);
            } else {
              log.warn(`[Resend Webhook] email.delivered — no invoice found for id: ${invoiceId}`);
            }
          } catch (dbErr: unknown) {
            log.error(`[Resend Webhook] Failed to update delivery_confirmed for invoice ${invoiceId}:`, (dbErr instanceof Error ? dbErr.message : String(dbErr)));
          }
        } else {
          log.info(`[Resend Webhook] email.delivered — no invoiceId tag, skipping delivery_confirmed update`);
        }
        break;
      }

      case "email.bounced": {
        // Hard bounces signal invalid/dead addresses. Auto-suppress to protect sender reputation.
        const bouncedAddresses: string[] = event.data.to ?? [];
        const bounceResendId: string = (event as any).data.id || '';
        log.error(`[Resend] Hard bounce for ${bouncedAddresses.join(", ")}`);
        for (const email of bouncedAddresses) {
          try {
            await db.insert(emailUnsubscribes)
              .values({
                email,
                workspaceId: null, // bounce applies globally across all workspaces
                unsubscribeAll: true,
                unsubscribeToken: crypto.randomBytes(32).toString('hex'),
                unsubscribeSource: 'bounce',
                unsubscribeReason: `Hard bounce reported by Resend — email ID: ${(event as any).data.id}`,
              })
              .onConflictDoNothing(); // unique constraint on (email, workspaceId) may fire
          } catch (err: unknown) {
            log.error('[Resend] Failed to suppress bounced address:', email, (err instanceof Error ? err.message : String(err)));
          }
        }
        // Update emailEvents record so delivery status reflects the actual outcome
        if (bounceResendId) {
          try {
            await db.update(emailEvents)
              .set({ status: 'bounced', updatedAt: new Date() })
              .where(eq(emailEvents.resendId, bounceResendId));
          } catch (err: unknown) {
            log.error('[Resend] Failed to update emailEvents on bounce:', bounceResendId, (err instanceof Error ? err.message : String(err)));
          }
        }
        // GAP-S30-01 FIX: Sync bounce outcome back to notification_deliveries (Phase 27 delivery tracking)
        // Marks any 'sent' or 'pending' email channel delivery records for these recipients as failed.
        if (bouncedAddresses.length > 0) {
          try {
            await db.update(notificationDeliveries)
              .set({
                status: 'failed',
                lastError: `Hard bounce reported by Resend — email ID: ${bounceResendId}`,
                updatedAt: new Date(),
              })
              .where(
                and(
                  inArray(notificationDeliveries.recipientUserId, bouncedAddresses),
                  eq(notificationDeliveries.channel, 'email'),
                  inArray(notificationDeliveries.status, ['sent', 'pending'])
                )
              );
          } catch (err: unknown) {
            log.error('[Resend] Failed to sync bounce to notification_deliveries:', (err instanceof Error ? err.message : String(err)));
          }
        }
        // Fire-and-forget rate check — logs CRITICAL alert if 24h bounce rate > threshold
        checkDeliverabilityRates().catch((err: any) => log.warn('[EventBus] Publish failed (non-blocking):', err?.message));
        break;
      }
        
      case "email.complained": {
        // Spam complaints damage sender reputation. Auto-suppress immediately.
        const complainedAddresses: string[] = event.data.to ?? [];
        const complaintResendId: string = (event as any).data.id || '';
        log.error(`[Resend] Spam complaint from ${complainedAddresses.join(", ")}`);
        for (const email of complainedAddresses) {
          try {
            await db.insert(emailUnsubscribes)
              .values({
                email,
                workspaceId: null, // complaint applies globally
                unsubscribeAll: true,
                unsubscribeToken: crypto.randomBytes(32).toString('hex'),
                unsubscribeSource: 'complaint',
                unsubscribeReason: `Spam complaint reported by Resend — email ID: ${(event as any).data.id}`,
              })
              .onConflictDoNothing();
          } catch (err: unknown) {
            log.error('[Resend] Failed to suppress complained address:', email, (err instanceof Error ? err.message : String(err)));
          }
        }
        // Update emailEvents record so delivery status reflects the actual outcome
        if (complaintResendId) {
          try {
            await db.update(emailEvents)
              .set({ status: 'complained', updatedAt: new Date() })
              .where(eq(emailEvents.resendId, complaintResendId));
          } catch (err: unknown) {
            log.error('[Resend] Failed to update emailEvents on complaint:', complaintResendId, (err instanceof Error ? err.message : String(err)));
          }
        }
        // GAP-S30-01 FIX: Sync complaint outcome back to notification_deliveries (Phase 27 delivery tracking)
        if (complainedAddresses.length > 0) {
          try {
            await db.update(notificationDeliveries)
              .set({
                status: 'failed',
                lastError: `Spam complaint reported by Resend — email ID: ${complaintResendId}`,
                updatedAt: new Date(),
              })
              .where(
                and(
                  inArray(notificationDeliveries.recipientUserId, complainedAddresses),
                  eq(notificationDeliveries.channel, 'email'),
                  inArray(notificationDeliveries.status, ['sent', 'pending'])
                )
              );
          } catch (err: unknown) {
            log.error('[Resend] Failed to sync complaint to notification_deliveries:', (err instanceof Error ? err.message : String(err)));
          }
        }
        // Fire-and-forget rate check — logs CRITICAL alert if 24h complaint rate > threshold
        checkDeliverabilityRates().catch((err: any) => log.warn('[EventBus] Publish failed (non-blocking):', err?.message));
        break;
      }
        
      default:
        break;
    }
    
    res.status(200).json({ received: true });
  } catch (error) {
    log.error("[Resend Webhook] Error processing event:", error);
    res.status(500).json({ error: "Webhook processing failed" });
  }
});

