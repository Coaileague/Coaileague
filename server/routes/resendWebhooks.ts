/**
 * Extract a Trinity reference number from an email subject or body.
 * Matches patterns like [Ref: TRN-XXXXXXXX] or Ref: TRN-XXXXXXXX
 */
function extractTrinityRefNumber(subject: string, body: string): string | null {
  const combined = `${subject || ''} ${body || ''}`;
  // Match our pre-reference format: SR-XXXXXXXXX-XXXX (generated by generatePreRef())
  const match = combined.match(/\[?Ref(?:erence)?:\s*(SR-[A-Z0-9]{4,12}-[A-Z0-9]{2,8})\]?/i);
  return match?.[1] ?? null;
}

/**
 * Detect if email body contains a complaint about service or officer
 * Returns true if the content is likely a complaint.
 */
function isLikelyComplaint(body: string): boolean {
  const lower = body.toLowerCase();
  const complaintSignals = [
    'complaint', 'complain', 'unacceptable', 'unprofessional', 'rude', 'late', 'no show',
    'did not show', 'didn\'t show', 'not satisfied', 'terrible', 'horrible', 'awful',
    'disrespectful', 'not happy', 'very upset', 'disappointed', 'issue with',
    'problem with the officer', 'problem with your officer', 'i want to report',
    'sleeping on duty', 'on the phone', 'was not there', 'wasn\'t there',
  ];
  return complaintSignals.some((sig) => lower.includes(sig));
}

/**
 * Detect if an inbound email is an officer shift-offer acceptance reply
 * Looks for pattern: YES [Full Name] [Phone] (case-insensitive, permissive spacing)
 */
function parseOfficerAcceptanceReply(body: string): { isAcceptance: boolean; name?: string; phone?: string } {
  const text = body.trim().replace(/\s+/g, ' ');
  const match = text.match(/^yes\s+([a-z'\-\s]{3,40})\s+([\d\s\(\)\-\+\.]{7,20})/i);
  if (!match) return { isAcceptance: false };
  const name = match[1].trim();
  const phone = match[2].replace(/[^\d\+]/g, '').slice(0, 15);
  return { isAcceptance: true, name, phone };
}

