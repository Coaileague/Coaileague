/**
 * Check if an email address matches staffing email configuration
 */
function isStaffingEmail(email: string): boolean {
  const normalizedEmail = email.toLowerCase().trim();
  if (normalizedEmail === STAFFING_EMAIL_CONFIG.address) return true;
  if (STAFFING_EMAIL_CONFIG.aliases.includes(normalizedEmail)) return true;
  return false;
}

/**
 * Extract email slug from subdomain format only.
 * Pattern: function@{slug}.coaileague.com → returns slug
 * All other formats (dash-alias, plus-addressing) are deprecated and not supported.
 */
function extractSlugFromEmail(email: ResendInboundEmail): string | null {
  for (const toAddr of (email.to || [])) {
    const domain = toAddr.split('@')[1] || '';
    if (domain.endsWith('.coaileague.com') && domain !== 'coaileague.com') {
      return domain.replace('.coaileague.com', '');
    }
  }
  return null;
}

/**
 * Extract workspace ID from email headers or routing (legacy support)
 */
function extractWorkspaceFromEmail(email: ResendInboundEmail): string | null {
  // Check for workspace ID in headers (X-Workspace-ID)
  if (email.headers?.['x-workspace-id']) {
    return email.headers['x-workspace-id'];
  }

  // Check for workspace-specific email pattern: staffing+workspaceId@coaileague.com (legacy UUID format)
  for (const toAddr of email.to || []) {
    const match = toAddr.match(/^staffing\+([a-f0-9-]{36})@/i);
    if (match) {
      return match[1];
    }
  }

  return null;
}

