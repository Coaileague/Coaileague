// Resend webhook signing secret for signature verification
const RESEND_WEBHOOK_SECRET = process.env.RESEND_WEBHOOK_SECRET;

/**
 * Verify Resend webhook signature using HMAC-SHA256 per Svix specification.
 *
 * Svix signed-content format (required):
 *   "{svix-id}.{svix-timestamp}.{rawBody}"
 *
 * The webhook signing secret is stored as a base64-encoded string (with an
 * optional "whsec_" prefix that must be stripped before decoding).
 */
function verifyResendSignature(
  payload: string,
  signature: string | undefined,
  timestamp: string | undefined,
  msgId: string | undefined
): boolean {
  // Fail-closed: if no secret is configured, REJECT all incoming webhooks.
  // This prevents unauthenticated webhook payloads from mutating platform state.
  if (!RESEND_WEBHOOK_SECRET) {
    log.error("[Resend Webhook] RESEND_WEBHOOK_SECRET is not set — rejecting webhook to prevent unauthenticated mutations. Configure the secret to enable webhook processing.");
    return false;
  }

  if (!signature || !timestamp || !msgId) {
    log.warn("[Resend Webhook] Missing svix-signature, svix-timestamp, or svix-id headers");
    return false;
  }

  // Replay protection: reject webhooks older than 5 minutes
  const timestampMs = parseInt(timestamp, 10) * 1000;
  if (isNaN(timestampMs) || Math.abs(Date.now() - timestampMs) > 5 * 60 * 1000) {
    log.warn("[Resend Webhook] Timestamp too old or invalid — possible replay attack");
    return false;
  }

  try {
    // Resend uses Svix format: v1,<base64-signature> (space-separated if multiple)
    const signatures = signature.split(" ");

    // Svix signed content: "{svix-id}.{svix-timestamp}.{rawBody}"
    const signedPayload = `${msgId}.${timestamp}.${payload}`;
    
    // Extract the actual secret (remove whsec_ prefix if present)
    const secretKey = RESEND_WEBHOOK_SECRET.startsWith("whsec_") 
      ? RESEND_WEBHOOK_SECRET.slice(6) 
      : RESEND_WEBHOOK_SECRET;
    
    // Svix stores the signing secret as base64-encoded bytes
    const secretBuffer = Buffer.from(secretKey, "base64");
    
    const expectedSig = crypto
      .createHmac("sha256", secretBuffer)
      .update(signedPayload)
      .digest("base64");
    
    for (const sig of signatures) {
      const [version, sigValue] = sig.split(",");
      
      if (version !== "v1" || !sigValue) continue;
      
      // Use constant-time comparison to prevent timing attacks
      try {
        if (crypto.timingSafeEqual(Buffer.from(sigValue), Buffer.from(expectedSig))) {
          return true;
        }
      } catch (compareError) {
        // Buffer length mismatch — signatures don't match, try next candidate
      }
    }
    
    log.warn(`[Resend Webhook] No matching signature found`);
    return false;
  } catch (error) {
    log.error("[Resend Webhook] Signature verification error:", error);
    return false;
  }
}

// ============================================================================
// DELIVERABILITY RATE MONITORING
// Fires on every bounce/complaint event and logs CRITICAL alert when
// bounce rate > 2% or complaint rate > 0.1% in a 24-hour window.
// These thresholds are industry standards: Google/Yahoo require <2% bounce,
// <0.1% complaint; exceeding them triggers deferrals or blocklisting.
// ============================================================================

const RATE_CHECK_WINDOW_MS = 24 * 60 * 60 * 1000; // 24 hours

export async function checkDeliverabilityRates(): Promise<void> {
  try {
    const since = new Date(Date.now() - RATE_CHECK_WINDOW_MS);
    const rows = await db.select({
      status: emailEvents.status,
      cnt: sql<number>`COUNT(*)::int`,
    })
    .from(emailEvents)
    .where(gte(emailEvents.createdAt, since))
    .groupBy(emailEvents.status);

    const totalSent = rows.reduce((sum, r) => sum + Number(r.cnt), 0);
    const bounces = rows.find(r => r.status === 'bounced');
    const complaints = rows.find(r => r.status === 'complained');
    const bounceCount = bounces ? Number(bounces.cnt) : 0;
    const complaintCount = complaints ? Number(complaints.cnt) : 0;

    if (totalSent === 0) return;

    const bounceRate = bounceCount / totalSent;
    const complaintRate = complaintCount / totalSent;

    if (bounceRate > EMAIL.bounceRateThreshold) {
      log.error(
        `[EMAIL DELIVERABILITY ALERT] CRITICAL: Bounce rate ${(bounceRate * 100).toFixed(2)}% exceeds threshold ${(EMAIL.bounceRateThreshold * 100).toFixed(1)}% ` +
        `(${bounceCount} bounces / ${totalSent} sent in last 24h). ` +
        `Immediate action required: review suppression list, remove invalid addresses, contact support@resend.com if sustained.`
      );
    }

    if (complaintRate > EMAIL.complaintRateThreshold) {
      log.error(
        `[EMAIL DELIVERABILITY ALERT] CRITICAL: Complaint rate ${(complaintRate * 100).toFixed(3)}% exceeds threshold ${(EMAIL.complaintRateThreshold * 100).toFixed(2)}% ` +
        `(${complaintCount} complaints / ${totalSent} sent in last 24h). ` +
        `Immediate action required: audit email content, check unsubscribe flow, and verify opt-in sources.`
      );
    }

    // Always log current rates at INFO level for observability
    log.info(
      `[Email Deliverability] 24h stats: ${totalSent} sent, ` +
      `${bounceCount} bounces (${(bounceRate * 100).toFixed(2)}%), ` +
      `${complaintCount} complaints (${(complaintRate * 100).toFixed(3)}%)`
    );
  } catch (err: unknown) {
    log.error('[Email Deliverability] Rate check failed:', err instanceof Error ? err.message : String(err));
  }
}

