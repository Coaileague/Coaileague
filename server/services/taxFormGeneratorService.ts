function addPDFDisclaimer(doc: PDFKit.PDFDocument) {
  doc.moveDown(1);
  doc.fontSize(7).font('Helvetica-Bold').fillColor('#CC0000');
  doc.text('DISCLAIMER', { align: 'center' });
  doc.fontSize(6).font('Helvetica').fillColor('#333333');
  for (const line of PLATFORM_DISCLAIMER) {
    doc.text(line, { align: 'center' });
  }
  doc.moveDown(0.3);
  doc.fontSize(6).fillColor('gray');
  doc.text(`Generated by CoAIleague on ${new Date().toLocaleString()}`, { align: 'center' });
}

