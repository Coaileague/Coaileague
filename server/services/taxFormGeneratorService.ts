function maskSSN(ssn: string): string {
  if (!ssn || ssn.length < 4) return '***-**-****';
  const last4 = ssn.replace(/\D/g, '').slice(-4);
  return `***-**-${last4}`;
}

function formatCurrency(amount: number): string {
  return amount.toFixed(2);
}

