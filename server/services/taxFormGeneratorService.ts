  async generate941Report(
    workspaceId: string,
    quarter: number,
    year: number
  ): Promise<{ success: boolean; pdfBuffer?: Buffer; taxFormId?: string; data?: Form941Data; error?: string }> {
    try {
      if (quarter < 1 || quarter > 4) {
        return { success: false, error: 'Quarter must be between 1 and 4' };
      }

      const workspace = await db.query.workspaces.findFirst({
        where: eq(workspaces.id, workspaceId),
      });
      if (!workspace) return { success: false, error: 'Workspace not found' };

      const quarterStartMonth = (quarter - 1) * 3;
      const quarterStart = new Date(year, quarterStartMonth, 1);
      const quarterEnd = new Date(year, quarterStartMonth + 3, 0, 23, 59, 59);

      const aggregated = await db
        .select({
          totalWages: sql<string>`COALESCE(SUM(CAST(${payrollEntries.grossPay} AS NUMERIC)), 0)`,
          federalTax: sql<string>`COALESCE(SUM(CAST(${payrollEntries.federalTax} AS NUMERIC)), 0)`,
          socialSecurity: sql<string>`COALESCE(SUM(CAST(${payrollEntries.socialSecurity} AS NUMERIC)), 0)`,
          medicare: sql<string>`COALESCE(SUM(CAST(${payrollEntries.medicare} AS NUMERIC)), 0)`,
          stateTax: sql<string>`COALESCE(SUM(CAST(${payrollEntries.stateTax} AS NUMERIC)), 0)`,
          employeeCount: sql<string>`COUNT(DISTINCT ${payrollEntries.employeeId})`,
        })
        .from(payrollEntries)
        .innerJoin(payrollRuns, eq(payrollEntries.payrollRunId, payrollRuns.id))
        .where(
          and(
            eq(payrollEntries.workspaceId, workspaceId),
            gte(payrollRuns.periodEnd, quarterStart),
            lte(payrollRuns.periodEnd, quarterEnd),
            inArray(payrollRuns.status, ['approved', 'processed', 'paid', 'completed'])
          )
        );

      const row = aggregated[0];
      const totalWages = parseFloat(row?.totalWages || '0');
      const federalTaxWithheld = parseFloat(row?.federalTax || '0');
      const employeeSS = parseFloat(row?.socialSecurity || '0');
      const employeeMedicare = parseFloat(row?.medicare || '0');
      const numberOfEmployees = parseInt(row?.employeeCount || '0');

      const SS_RATE = 0.062;
      const MEDICARE_RATE = 0.0145;
      const ADDITIONAL_MEDICARE_RATE = 0.009;

      // F060 fix: Calculate Additional Medicare Tax (line 5d) from actual withheld amounts.
      // The stored medicare column includes both regular (1.45%) and additional (0.9%) medicare.
      // Regular expected = totalWages * 1.45%; difference = additional medicare withheld.
      const regularMedicareExpected = totalWages * MEDICARE_RATE;
      const additionalMedicareWithheld = Math.max(0, employeeMedicare - regularMedicareExpected);
      const additionalMedicareWages = additionalMedicareWithheld > 0
        ? Math.round((additionalMedicareWithheld / ADDITIONAL_MEDICARE_RATE) * 100) / 100
        : 0;

      const employerSS = totalWages * SS_RATE;
      const employerMedicare = totalWages * MEDICARE_RATE;
      const totalSSTax = employeeSS + employerSS;
      // Line 5c tax = regular employee + employer medicare (additional medicare is employee-only, reported on 5d)
      const regularEmployeeMedicare = employeeMedicare - additionalMedicareWithheld;
      const totalMedicareTax = regularEmployeeMedicare + employerMedicare;
      // Line 5d tax = additional medicare (employee-only, 0.9%)
      const additionalMedicareTax = additionalMedicareWithheld;
      const totalSSMedicareTaxes = totalSSTax + totalMedicareTax + additionalMedicareTax;
      const totalTaxesBeforeAdjustments = federalTaxWithheld + totalSSMedicareTaxes;
      const totalTaxesAfterAdjustments = totalTaxesBeforeAdjustments;
      const balanceDue = totalTaxesAfterAdjustments;

      const form941Data: Form941Data = {
        employerEIN: (workspace as any).taxId || (workspace as any).ein || 'XX-XXXXXXX',
        employerName: (workspace as any).companyName || workspace.name || 'Employer',
        employerAddress: (workspace as any).address || '',
        quarter,
        taxYear: year,
        line1_numberOfEmployees: numberOfEmployees,
        line2_wagesTipsCompensation: totalWages,
        line3_federalIncomeTaxWithheld: federalTaxWithheld,
        line5a_taxableSocialSecurityWages: totalWages,
        line5a_tax: totalSSTax,
        line5b_taxableSocialSecurityTips: 0,
        line5b_tax: 0,
        line5c_taxableMedicareWages: totalWages,
        line5c_tax: totalMedicareTax,
        line5d_taxableMedicareWagesOverLimit: additionalMedicareWages,
        line5d_tax: additionalMedicareTax,
        line5e_totalSocialSecurityMedicareTaxes: totalSSMedicareTaxes,
        line6_totalTaxesBeforeAdjustments: totalTaxesBeforeAdjustments,
        line10_totalTaxesAfterAdjustments: totalTaxesAfterAdjustments,
        line11_totalDepositsForQuarter: 0,
        line14_balanceDueOrOverpayment: balanceDue,
        employerSSRate: SS_RATE,
        employerMedicareRate: MEDICARE_RATE,
      };

      const pdfBuffer = await this.generate941PDF(form941Data);

      let taxFormId: string | undefined;
      try {
        const allEmps = await db
          .select({ id: employees.id })
          .from(employees)
          .where(eq(employees.workspaceId, workspaceId))
          .limit(1);

        if (allEmps.length > 0) {
          const [taxForm] = await db.insert(employeeTaxForms).values({
            workspaceId,
            employeeId: allEmps[0].id,
            formType: '941',
            taxYear: year,
            wages: String(totalWages),
            federalTaxWithheld: String(federalTaxWithheld),
            socialSecurityWages: String(totalWages),
            socialSecurityTaxWithheld: String(employeeSS + employerSS),
            medicareWages: String(totalWages),
            medicareTaxWithheld: String(employeeMedicare + employerMedicare),
            stateTaxWithheld: String(0),
            generatedAt: new Date(),
            isActive: true,
          }).returning();
          taxFormId = taxForm.id;
        }
      } catch (storageErr) {
        log.warn('[TaxFormGenerator] Could not store 941 record (enum may need migration):', storageErr);
      }

      // Stamp + save to vault
      const workspace941 = await db.query.workspaces?.findFirst?.({ where: (w: any, { eq: e }: any) => e(w.id, workspaceId) });
      const vaultResult941 = await saveToVault({
        workspaceId,
        workspaceName: (workspace941 as any)?.name || workspaceId,
        documentTitle: `Form 941 Employer Quarterly Federal Tax Return — Q${quarter} ${year}`,
        category: 'tax',
        formNumber: '941',
        period: `Q${quarter} ${year}`,
        relatedEntityType: 'workspace',
        relatedEntityId: workspaceId,
        rawBuffer: pdfBuffer,
      });
      if (!vaultResult941.success) log.warn('[TaxFormGenerator] 941 vault save failed:', vaultResult941.error);

      return {
        success: true,
        pdfBuffer: vaultResult941.stampedBuffer || pdfBuffer,
        taxFormId,
        data: form941Data,
        vaultId: vaultResult941.vault?.id,
        documentNumber: vaultResult941.vault?.documentNumber,
      };
    } catch (error) {
      log.error('[TaxFormGenerator] 941 generation failed:', error);
      return { success: false, error: String(error) };
    }
  }

