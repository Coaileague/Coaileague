/**
 * Workspace ID Index Bootstrap
 *
 * Per TRINITY.md §9 (Schema Completeness):
 * "All workspace_id columns indexed."
 *
 * Phase I audit (2026-04-07) found 507 multi-tenant tables that
 * declare a workspace_id column but lack a leading workspaceId index
 * in the Drizzle schema. Without it, every per-workspace lookup falls
 * back to a sequential scan, which becomes a tail-latency cliff at
 * scale and a noisy-neighbour vector that lets one workspace degrade
 * query performance for the rest.
 *
 * This module installs `<table>_workspace_idx` on every such table at
 * boot, idempotently. The full list is generated mechanically from
 * the Drizzle schema and pasted in below — keep it in sync when you
 * add new per-tenant tables.
 *
 * Each statement is `CREATE INDEX IF NOT EXISTS` and skips tables
 * that don't yet exist in the live DB. Failures are logged but never
 * block boot — these are performance indexes, not correctness invariants.
 *
 * 🟡 Long-term fix: declare these indexes in the Drizzle schema files
 * directly so SchemaParityService can manage them. This bootstrap is
 * the transitional shim while the per-domain schema files are updated.
 */

