export async function ensureWorkspaceIndexes(): Promise<void> {
  log.info(`[workspaceIndex] Ensuring workspace_id indexes on ${TABLES_NEEDING_WORKSPACE_INDEX.length} tables`);

  let installed = 0;
  let alreadyPresent = 0;
  let skipped = 0;
  let failed = 0;

  for (const table of TABLES_NEEDING_WORKSPACE_INDEX) {
    // Defense-in-depth: validate identifier even though list is hardcoded
    if (!/^[a-z0-9_]+$/.test(table)) {
      log.warn(`[workspaceIndex] Skipping invalid table name: ${table}`);
      continue;
    }
    const indexName = `${table}_workspace_idx`;
    if (indexName.length > 63) {
      log.warn(`[workspaceIndex] Index name too long, skipping: ${indexName}`);
      continue;
    }
    try {
      // Check the table actually exists before trying to index it.
      // Some tables in the list may belong to features not yet deployed
      // in this environment.
      const { rows: tableExists } = await pool.query(
        `SELECT 1 FROM information_schema.tables
         WHERE table_schema = current_schema() AND table_name = $1`,
        [table]
      );
      if (tableExists.length === 0) {
        skipped++;
        continue;
      }
      // Verify the column actually exists too
      const { rows: colExists } = await pool.query(
        `SELECT 1 FROM information_schema.columns
         WHERE table_schema = current_schema() AND table_name = $1 AND column_name = 'workspace_id'`,
        [table]
      );
      if (colExists.length === 0) {
        skipped++;
        continue;
      }
      // Idempotent: pg_indexes lookup avoids the noise of CREATE IF NOT EXISTS
      const { rows: idxExists } = await pool.query(
        `SELECT 1 FROM pg_indexes
         WHERE schemaname = current_schema() AND indexname = $1`,
        [indexName]
      );
      if (idxExists.length > 0) {
        alreadyPresent++;
        continue;
      }
      await pool.query(`CREATE INDEX IF NOT EXISTS "${indexName}" ON "${table}" ("workspace_id")`);
      installed++;
    } catch (err: unknown) {
      failed++;
      log.warn(`[workspaceIndex] ${table}: ${err?.message}`);
    }
  }

  log.info(
    `[workspaceIndex] Complete: ${installed} installed, ${alreadyPresent} already present, ${skipped} skipped (table missing), ${failed} failed`
  );
}
